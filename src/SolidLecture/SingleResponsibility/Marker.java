package SolidLecture.SingleResponsibility;

public class Marker {
    String brand;
    String modelName;
    String color;
    int price;

    public Marker(String brand, String modelName, String color, int price) {
        this.brand = brand;
        this.modelName = modelName;
        this.color = color;
        this.price = price;
    }
}
